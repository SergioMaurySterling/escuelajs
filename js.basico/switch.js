//forma de validar y generar condiciones, hace lo mismo que el if
//para validar casos

var numero = 1000;

switch(numero){
    case 1:
        console.log('soy uno');
        break;
    case 10:
        console.log('soy un 10');
        break;
    case 1000: 
        console.log('soy un 1000');
        break;
    default: 
        console.log('ninguno numero caso');
}